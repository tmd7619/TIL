CREATE TABLE TEST_COMPOSIT_PK(  
    ID      VARCHAR2(50),
    NAME    VARCHAR2(50),
    SALARY  NUMBER      CHECK (SALARY > 0)  ,     -- (7)을 해주면 넘버의 자릿수 지정 ,2를 하면 2자리는 실수형으로 쓰겠다는 뜻
    GENDER  CHAR(1)     CHECK (GENDER IN ('M' , 'F')) ,  
    PRIMARY KEY(ID, NAME)
)

