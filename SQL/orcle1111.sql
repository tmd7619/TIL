FROM    TEST_COMPOSIT_PK    


CREATE TABLE TEST_COMPOSIT_FK(
    PID     VARCHAR2(50) PRIMARY KEY,
    ID      VARCHAR2(50),
    NAME    VARCHAR2(50),
    FOREIGN KEY(ID, NAME) REFERENCES TEST_COMPOSIT_PK (ID, NAME)
)

